<h3>1.2 Inertial Observer</h3>
<div>
If the frame of the observer is accelerating then Newton's law is no longer invariant.
Therfore, all reference frames (together with their observers) that do not have any acceleration are called
"Inertial Frames" (or "Inertial Observers").
    <br>
Note, in this case, an observer can detect whether his/her reference frame is inertial. Otherwise, for example,
he would see liquids sloshing around.
</div>

