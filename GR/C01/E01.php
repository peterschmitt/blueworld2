<h3>Exercise 1</h3>
<strong>Convert the following to units in which c = 1, expressing everything in terms of m
and kg:</strong>
<br>
<a href="http://en.wikipedia.org/wiki/International_System_of_Units">Wikipedia Units of Measure</a>
The action is to convert every unit of second by unit of meter:

$$ c = 1 \rightarrow  1 \,s = 3 * 10^{8}*m $$
a. How much is 10 Joule?
<div class="indent1">
    Well, the units of Joule are \( kg * \frac{m}{s^{2}} * m \).<br>
    $$ 10 \,J = 10 \,kg * \frac{1}{9*10^{16}} = 1.1 * 10^{-16} kg $$.
    <strong style="border: 1px solid;padding: 10px">Note: \(1 \,J = 1.1 * 10^{-17} kg\) ! </strong>
</div>
<br>

b. The power output of 100 W.
<div class="indent1">
    $$100 \,Watt = 100 \frac{J}{s} = 100 * \frac{1.1 * 10^{-17} kg}{3*10^{8}\,m}= 3.7 * 10^{-24} \frac{kg}{m} $$
</div>
<br>

(c) Planck’s reduced constant, = 1.05 × 10−34 J s. (Note the definition of in terms
of Planck’s constant h: = h/2π.)
<div class="indent1">
$$1.05 * 10^{-34} J \, s = 1.05 * 10^{-34} * 1.1 *10^{-17} kg * \, 3 * 10^{8} m = $$
    $$  = 3.5 * 10^{-43} kg\,m$$
</div>
<br>

(d) Velocity of a car, v = 30 m s−1 .
<div class="indent1">
$$ v = 30\,\frac{m}{s}\,=\,30*\frac{1}{3*10^{8}}\frac{s}{m} = 10^{-7}.$$
     (no units, just about 10 millions the of speed of light)
</div>
<br>

(e) Momentum of a car, 3 × 104 kg m s−1 .
<div class="indent1">
Momentum has the same units than energy:<br>
    $$3*104\,kg \frac{m}{s} = 3*104\,kg \frac{m}{s} * \frac{s}{3*10^{8} m} = 1.04 * 10^{-6} kg $$<br>
    Does that mean I need 1 mg of matter to accelerate 104 kg to 3 \(\frac{m}{s}\)?
</div>
<br>


(f) Pressure of one atmosphere = 105 N m−2 .
<div class="indent1">
$$ 105 N \frac{1}{m^{2}} = 105 \frac{kg*m}{s^{2}} * \frac{1}{m^{2}} = 105 \frac{kg * m}{9*10^{16} m^{2} \, m^{2}}
     = 1.17 * 10^{-15}\frac{kg}{m}$$
</div>
<br>

(g) Density of water, 103 kg m−3 .
Nothing to convert here.
<div class="indent1">

</div>
<br>

(h) Luminosity flux 106 J s−1 cm−2 .
<div class="indent1">
    $$ 106\,\frac{J}{s \, cm^{2}} = 106\,\frac{1.1*10^{-17}kg}{3*10^{8}m*10^4*m^{-2}} $$
    $$ 106 * \frac{1.1}{3} * 10^{-21} \frac{kg}{ m^{3}} = 3.9 * 10^{-20} \frac{kg}{m^{3}} $$
</div>
